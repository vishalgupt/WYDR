package wydr.sellers.gson;

import com.google.gson.JsonElement;
import com.google.gson.annotations.SerializedName;

import java.util.List;

/**
 * Created by Deepesh_pc on 26-02-2016.
 */
public class ProductHolder {
    @SerializedName("params")
    JsonElement params;
    @SerializedName("products")
    private List<JsonElement> products;

    public JsonElement getParams() {
        return params;
    }

    public void setParams(JsonElement params) {
        this.params = params;
    }

    public List<JsonElement> getProducts() {
        return products;
    }

    public void setProducts(List<JsonElement> products) {
        this.products = products;
    }
}
