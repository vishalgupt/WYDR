package wydr.sellers.gson;

import com.google.gson.annotations.SerializedName;

/**
 * Created by Deepesh_pc on 15-02-2016.
 */
public class FavProdModal {

    @SerializedName("product_id")
    public String id;
    @SerializedName("product_code")
    public String code;
    @SerializedName("list_price")
    public String price;
    @SerializedName("price")
    public String sp;
    @SerializedName("product")
    public String name;
    @SerializedName("amount")
    public String qty;
    @SerializedName("min_qty")
    public String moq;
    @SerializedName("user_login")
    public String userid;
    @SerializedName("company_id")
    public String companyId;
    @SerializedName("price_share")
    public String chatForPrice;

    public String getChatForPrice() {
        return chatForPrice;
    }

    public void setChatForPrice(String chatForPrice) {
        this.chatForPrice = chatForPrice;
    }

    @SerializedName("thumbnails")
    public ThumbImage thumbnails;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getSp() {
        return sp;
    }

    public void setSp(String sp) {
        this.sp = sp;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getQty() {
        return qty;
    }

    public void setQty(String qty) {
        this.qty = qty;
    }

    public String getMoq() {
        return moq;
    }

    public void setMoq(String moq) {
        this.moq = moq;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getCompanyId() {
        return companyId;
    }

    public void setCompanyId(String companyId) {
        this.companyId = companyId;
    }

    public ThumbImage getThumbnails() {
        return thumbnails;
    }

    public void setThumbnails(ThumbImage thumbnails) {
        this.thumbnails = thumbnails;
    }
}
