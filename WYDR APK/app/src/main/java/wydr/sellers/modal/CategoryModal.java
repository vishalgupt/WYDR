public class CategoryModal {

    private String productname, productcode, price;
    private Integer units, imagev;


    public CategoryModal() {
        this.productname = "";
        this.productcode = "";
        this.units = 0;
        this.imagev = 0;
        this.price = "";

    }


    public String getproductname() {
        return productname;
    }

    public void setproductname(String name) {
        this.productname = name;
    }

    public String getproductcode() {
        return productcode;
    }

    public void setproductcode(String number) {
        this.productcode = number;
    }

    public String getprice() {
        return price;
    }

    public void setprice(String id) {
        this.price = id;
    }

    public Integer getunits() {
        return units;
    }

    public void setUnits(Integer units) {
        this.units = units;
    }

    public Integer getImage() {
        return imagev;
    }

    public void setImage(Integer units) {
        this.imagev = units;
    }

}

