package wydr.sellers.modal;

import java.io.Serializable;

/**
 * Created by Deepesh_pc on 09-07-2015.
 */
public class UserProfile implements Serializable {

    private String username, useraddress, useremail, usercontact, usercategory, usercompany, usercompany_id, userid, id, user_net_id, user_imgUrl;
    private Boolean isFav = false;

    public UserProfile() {
        this.username = "";
        this.useraddress = "";
        this.useremail = "";
        this.usercontact = "";
        this.usercategory = "";
        this.usercompany = "";
    }

    public String getUser_imgUrl() {
        return user_imgUrl;
    }

    public void setUser_imgUrl(String user_imgUrl) {
        this.user_imgUrl = user_imgUrl;
    }

    public String getUsercompany_id() {
        return usercompany_id;

    }

    public void setUsercompany_id(String usercompany_id) {
        this.usercompany_id = usercompany_id;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUser_net_id() {
        return user_net_id;
    }

    public void setUser_net_id(String user_net_id) {
        this.user_net_id = user_net_id;
    }

    public Boolean getIsFav() {
        return isFav;
    }

    public void setIsFav(Boolean isFav) {
        this.isFav = isFav;
    }

    public String getUsercompany() {
        return usercompany;
    }

    public void setUsercompany(String usercompany) {
        this.usercompany = usercompany;
    }

    public String getusername() {
        return username;
    }

    public void setusername(String name) {
        this.username = name;
    }

    public String getuseraddress() {
        return useraddress;
    }

    public void setuseraddress(String number) {
        this.useraddress = number;
    }

    public String getuseremail() {
        return useremail;
    }

    public void setuseremail(String id) {
        this.useremail = id;
    }

    public String getusercontact() {
        return usercontact;
    }

    public void setusercontact(String id) {
        this.usercontact = id;
    }

    public String getUserCategory() {
        return usercategory;
    }

    public void setUserCategory(String id) {
        this.usercategory = id;
    }

}

